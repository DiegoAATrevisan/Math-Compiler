// The export function is a utility function for subtracting two numbers.
// The function takes in two parameters, num1 and num2, which represent the two numbers to be subtracted.
export function subtrai(num1, num2) {
    // Inside the function, the code simply subtracts num2 from num1.
    // This resulting value is then returned by the function.
    return num1 - num2;
}